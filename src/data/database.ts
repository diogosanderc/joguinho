import type { Loan } from '../utils/loanEngine';

// GOL Goleiro, ZAG Zagueiro, LD Lateral-Direito, LE Lateral-Esquerdo,
// VOL Volante, MEI Meia, PON Ponta, CA Centroavante
export type PlayerPosition = 'GOL' | 'ZAG' | 'LD' | 'LE' | 'VOL' | 'MEI' | 'PON' | 'CA';

export const getPositionGroup = (position: PlayerPosition): 'GK' | 'DF' | 'MF' | 'FW' => {
  if (position === 'GOL') return 'GK';
  if (position === 'ZAG' || position === 'LD' || position === 'LE') return 'DF';
  if (position === 'VOL' || position === 'MEI') return 'MF';
  return 'FW'; // PON, CA
};

export interface Player {
  id: string;
  name: string;
  age: number;
  position: PlayerPosition;
  rating: number;
  energy: number;
  value: number;
  salary: number;
  goals: number;
  yellowCards: number;
  redCards: number;
  isInjured: boolean;
  injuryWeeks?: number;
  isStar: boolean;
  contractLocked: boolean;
  contractWeeks?: number;
  benchRounds?: number;
  contractLockYears?: number; // Number of years contract is locked
  performanceTrend?: 'UP' | 'DOWN' | 'NEUTRAL'; // Performance trend indicators
  suspendedMatches?: number; // League matches still missed due to a red card or 3rd accumulated yellow
  lockCooldown?: boolean; // Has used up their contract lock; blocked from locking again until a fresh renewal
}

// A player is fit to be selected: not injured, and not serving a card suspension.
export const isPlayerAvailable = (p: Player): boolean =>
  !p.isInjured && !(p.suspendedMatches && p.suspendedMatches > 0);

export interface Club {
  id: string;
  name: string;
  division: 'A' | 'B' | 'C';
  primaryColor: string;
  secondaryColor: string;
  textColor: string; // Light or dark text compatibility
  stadiumCapacity: number;
  stadiumName: string;
  ticketPrice: number;
  finances: number;
  confidence: number;
  reputation: number; // 1-100
  isPlayerClub: boolean;
  squad: Player[];
  penaltyTakerId?: string; // Player designated to take penalty kicks
  hasVipBoxes?: boolean; // Premium seating built -- adds a flat revenue bonus per home match
  vipBoxesWeeksLeft?: number; // Weeks remaining while VIP boxes are under construction
  financialScore?: number; // 0-100 "Score Financeiro" driving bank loan terms
  loans?: Loan[]; // Active bank loans
  lateStrikes?: number; // Cumulative missed installments -- 3+ blocks new loans
  lastSeasonRevenue?: number; // Basis for the bank credit limit calculation
  seasonRevenueAccum?: number; // Running total of this season's income, becomes lastSeasonRevenue at season end
  seasonStartFinances?: number; // Cash snapshot at the start of the season, to detect profit/loss for the Score Financeiro
}

