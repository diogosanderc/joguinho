const randomRange = (min: number, max: number) => Math.floor(Math.random() * (max - min + 1)) + min;

const calculatePlayerValueAndSalary = (rating: number, age: number, position: PlayerPosition) => {
  const ageFactor = age < 24 ? 1.3 : age > 30 ? 0.7 : 1.0;
  const group = getPositionGroup(position);
  const positionFactor = group === 'FW' ? 1.2 : group === 'GK' ? 0.9 : 1.0;
  const ratingBase = Math.pow(rating - 30, 2.5) * 800;
  const value = Math.max(10000, Math.round(ratingBase * ageFactor * positionFactor));
  const salary = Math.round(value * 0.005);
  return { value, salary };
};

const MIN_PLAYER_AGE = 22;
const MAX_PLAYER_AGE = 34;

export const generateSquad = (clubId: string, stars: { name: string; position: PlayerPosition; rating: number; age?: number }[] = []): Player[] => {
  const squad: Player[] = [];
  let idCounter = 1;

  stars.forEach(star => {
    // Never field anyone under 22
    if (star.age !== undefined && star.age < MIN_PLAYER_AGE) return;
    const age = star.age !== undefined ? star.age : randomRange(MIN_PLAYER_AGE, MAX_PLAYER_AGE);

    const { value, salary } = calculatePlayerValueAndSalary(star.rating, age, star.position);
    squad.push({
      id: `${clubId}_p_${idCounter++}`,
      name: star.name,
      age,
      position: star.position,
      rating: star.rating,
      energy: 100,
      value,
      salary,
      goals: 0,
      yellowCards: 0,
      redCards: 0,
      isInjured: false,
      isStar: star.rating >= 80,
      contractLocked: star.rating >= 83
    });
  });

  const posOrder: Record<PlayerPosition, number> = { GOL: 0, ZAG: 1, LD: 2, LE: 3, VOL: 4, MEI: 5, PON: 6, CA: 7 };
  return squad.sort((a, b) => {
    if (posOrder[a.position] !== posOrder[b.position]) {
      return posOrder[a.position] - posOrder[b.position];
    }
    return b.rating - a.rating;
  });
};

export const initializeClubs = (): Club[] => {
  return CLUB_DEFINITIONS.map(def => {
    const squad = generateSquad(def.id, def.stars);
    const tvMoney = def.division === 'A' ? 8000000 : def.division === 'B' ? 2000000 : 500000;
    const finances = Math.round(def.reputation * def.reputation * 3000) + tvMoney;
    const ticketPrice = def.division === 'A' ? 50 : def.division === 'B' ? 35 : 25;

    // Rough estimate of a season's revenue (ticket + merch + sponsor-ish + TV money), used as
    // the bank credit limit basis until real tracked income replaces it after year 1.
    const ticketRevenueEstimate = Math.round(def.stadiumCapacity * 0.7 * ticketPrice) * 19;
    const merchRevenueEstimate = Math.round(def.reputation * 60 * 0.9) * 38;
    const sponsorRevenueEstimate = Math.round(def.reputation * 90) * 38;
    const lastSeasonRevenue = ticketRevenueEstimate + merchRevenueEstimate + sponsorRevenueEstimate + tvMoney;

    return {
      id: def.id,
      name: def.name,
      division: def.division,
      primaryColor: def.primaryColor,
      secondaryColor: def.secondaryColor,
      textColor: def.textColor,
      stadiumCapacity: def.stadiumCapacity,
      stadiumName: def.stadiumName,
      ticketPrice,
      finances,
      confidence: 70,
      reputation: def.reputation,
      isPlayerClub: false,
      financialScore: 70,
      loans: [],
      lateStrikes: 0,
      lastSeasonRevenue,
      seasonRevenueAccum: 0,
      seasonStartFinances: finances,
      squad
    };
  });
};

export const formatCurrency = (value: number): string => {
  if (Math.abs(value) >= 1000000) {
    const val = value / 1000000;
    return `R$ ${val % 1 === 0 ? val.toFixed(0) : val.toFixed(1)} M`;
  }
  if (Math.abs(value) >= 1000) {
    const val = value / 1000;
    return `R$ ${val % 1 === 0 ? val.toFixed(0) : val.toFixed(1)} mil`;
  }
  return `R$ ${value}`;
};
